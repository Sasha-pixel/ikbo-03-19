package ru.mirea.prac10.task1;

public class Complex {
    int real;
    int imagine;

    public Complex(int real, int imagine) {
        this.real = real;
        this.imagine = imagine;
    }

    public int getReal() {
        return this.real;
    }

    public int getImagine() {
        return this.imagine;
    }
}
